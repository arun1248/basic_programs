package oops.oopstask;

public class Book {

    int bookId;
    String title;
    String author;
    boolean isAvailable;

    public Book(int bookId, String title, String author, boolean isAvailable) {
        this.bookId = bookId;
        this.title = title;
        this.author = author;
        this.isAvailable = isAvailable;
    }
}
